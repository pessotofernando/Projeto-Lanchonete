CREATE PROCEDURE CONTROLE_SP_SEL
(
@INP_SIGLA	VARCHAR(03)	
)

WITH ENCRYPTION 
AS


	TRUNCATE TABLE FORMULARIO_AUX


	INSERT INTO FORMULARIO_AUX	
		(
			FOR_NOME	,
			FOR_DESCRI	,
			FOR_SELECI	
		)


	SELECT	FOR_NOME	,
			FOR_DESCRI	,
			''		
	FROM	FORMULARIO	WITH(NOLOCK)



	UPDATE 	FORMULARIO_AUX SET
			FOR_SELECI	=	'X'
	FROM	CONTROLE
	WHERE	CONTROLE.FOR_NOME	=	FORMULARIO_AUX.FOR_NOME	AND
			CONTROLE.USU_SIGLA	=	@INP_SIGLA		


RETURN


